<?php
/**
 * One-shot web installer for Devithor LMS backend.
 *
 *  Visit https://your-domain/install.php after uploading the repo. The wizard
 *  walks you through:
 *    1. Server requirements check
 *    2. Database credentials → writes .env
 *    3. Schema migrations
 *    4. First admin user
 *    5. Lock-down — install.php refuses to run again until you delete the
 *       `.installed` flag file.
 *
 *  IMPORTANT: After step 5, **delete this file** from the server. Even with
 *  the lock in place, leaving a public installer endpoint is bad hygiene.
 */

declare(strict_types=1);

ini_set('display_errors', '1');
error_reporting(E_ALL);

$projectRoot    = dirname(__DIR__);
$envPath        = $projectRoot . '/.env';
$lockPath       = $projectRoot . '/.installed';
$migrationsDir  = $projectRoot . '/migrations';

$step = $_GET['step'] ?? 'welcome';

// ---- Lock-out: refuse to do anything once installed --------------------
if (file_exists($lockPath) && $step !== 'locked') {
    header('Location: install.php?step=locked');
    exit;
}

// ---- Action handlers ----------------------------------------------------
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $action = $_POST['action'] ?? '';
    try {
        if ($action === 'save_env') {
            handleSaveEnv($envPath);
            redirect('install.php?step=migrate');
        }
        if ($action === 'migrate') {
            $applied = runMigrations($envPath, $migrationsDir);
            renderPage('Migrations', migrateSuccessHtml($applied));
            exit;
        }
        if ($action === 'create_admin') {
            handleCreateAdmin($envPath, $lockPath);
            redirect('install.php?step=done');
        }
    } catch (Throwable $e) {
        renderPage('Error', errorHtml($e->getMessage()));
        exit;
    }
}

// ---- Page router --------------------------------------------------------
switch ($step) {
    case 'requirements': renderPage('Requirements', requirementsHtml()); break;
    case 'db':           renderPage('Database',     dbFormHtml($envPath)); break;
    case 'migrate':      renderPage('Migrations',   migratePromptHtml()); break;
    case 'admin':        renderPage('Admin user',   adminFormHtml()); break;
    case 'done':         renderPage('Done',         doneHtml()); break;
    case 'locked':       renderPage('Already installed', lockedHtml($lockPath)); break;
    default:             renderPage('Welcome',      welcomeHtml());
}
exit;

// =======================================================================
//  ACTION HANDLERS
// =======================================================================

function handleSaveEnv(string $envPath): void
{
    $needed = ['DB_HOST', 'DB_PORT', 'DB_DATABASE', 'DB_USERNAME', 'DB_PASSWORD', 'APP_URL'];
    $vals = [];
    foreach ($needed as $k) {
        $v = trim((string) ($_POST[$k] ?? ''));
        if ($v === '' && $k !== 'DB_PASSWORD') {
            throw new RuntimeException("Field $k is required.");
        }
        $vals[$k] = $v;
    }

    // Sanity-check the connection BEFORE writing .env so we don't lock the
    // user into a bad config that needs SSH to fix.
    try {
        $dsn = "mysql:host={$vals['DB_HOST']};port={$vals['DB_PORT']};dbname={$vals['DB_DATABASE']};charset=utf8mb4";
        new PDO($dsn, $vals['DB_USERNAME'], $vals['DB_PASSWORD'], [
            PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION,
        ]);
    } catch (PDOException $e) {
        throw new RuntimeException('Could not connect to the database with those credentials. Double-check host, name, user, and password in hPanel. (' . $e->getMessage() . ')');
    }

    // Auto-generate APP_KEY so the user never has to think about it.
    $appKey  = base64_encode(random_bytes(48));
    $stamp   = date('c');
    $appName = 'Devithor LMS';

    $env = <<<ENV
# Generated by install.php on $stamp
APP_NAME="$appName"
APP_ENV=production
APP_DEBUG=false
APP_URL={$vals['APP_URL']}
APP_TIMEZONE=UTC
APP_KEY=$appKey

DB_HOST={$vals['DB_HOST']}
DB_PORT={$vals['DB_PORT']}
DB_DATABASE={$vals['DB_DATABASE']}
DB_USERNAME={$vals['DB_USERNAME']}
DB_PASSWORD={$vals['DB_PASSWORD']}
DB_CHARSET=utf8mb4

CORS_ALLOWED_ORIGINS=
LOG_LEVEL=info
ENV;

    if (file_put_contents($envPath, $env) === false) {
        throw new RuntimeException("Couldn't write .env to $envPath. The web server user needs write permission on the project root.");
    }
    @chmod($envPath, 0600);
}

function runMigrations(string $envPath, string $migrationsDir): array
{
    if (!is_file($envPath)) {
        throw new RuntimeException('.env not found. Go back to step 2.');
    }

    $env = parseEnv($envPath);
    $pdo = pdoFrom($env);

    $pdo->exec(
        'CREATE TABLE IF NOT EXISTS schema_migrations (
            filename VARCHAR(255) PRIMARY KEY,
            applied_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
        ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4'
    );

    $applied = [];
    $stmt = $pdo->query('SELECT filename FROM schema_migrations');
    while ($row = $stmt->fetch(PDO::FETCH_ASSOC)) {
        $applied[$row['filename']] = true;
    }

    $files = glob($migrationsDir . '/*.sql') ?: [];
    sort($files);
    $newlyApplied = [];

    foreach ($files as $file) {
        $name = basename($file);
        if (isset($applied[$name])) continue;
        $sql = (string) file_get_contents($file);
        if (trim($sql) === '') continue;
        foreach (preg_split('/;\s*$/m', $sql) as $stmt) {
            $stmt = trim($stmt);
            if ($stmt === '') continue;
            $pdo->exec($stmt);
        }
        $insert = $pdo->prepare('INSERT INTO schema_migrations (filename) VALUES (?)');
        $insert->execute([$name]);
        $newlyApplied[] = $name;
    }

    return $newlyApplied;
}

function handleCreateAdmin(string $envPath, string $lockPath): void
{
    $email    = strtolower(trim((string) ($_POST['email'] ?? '')));
    $name     = trim((string) ($_POST['full_name'] ?? ''));
    $password = (string) ($_POST['password'] ?? '');
    $confirm  = (string) ($_POST['password_confirm'] ?? '');

    if (!filter_var($email, FILTER_VALIDATE_EMAIL)) throw new RuntimeException('Enter a valid email.');
    if (mb_strlen($name) < 2)                       throw new RuntimeException('Name is too short.');
    if (mb_strlen($password) < 12)                  throw new RuntimeException('Password must be at least 12 characters.');
    if ($password !== $confirm)                     throw new RuntimeException("Passwords don't match.");

    $env = parseEnv($envPath);
    $pdo = pdoFrom($env);

    $existing = $pdo->prepare('SELECT id FROM users WHERE email = ?');
    $existing->execute([$email]);
    $row = $existing->fetch(PDO::FETCH_ASSOC);

    $hash = password_hash($password, PASSWORD_DEFAULT);
    if ($row) {
        // Promote the existing user to admin and reset password.
        $up = $pdo->prepare('UPDATE users SET role = ?, password_hash = ?, full_name = ? WHERE id = ?');
        $up->execute(['ADMIN', $hash, $name, $row['id']]);
    } else {
        $id = 'u_' . bin2hex(random_bytes(8));
        $ins = $pdo->prepare('INSERT INTO users (id, email, full_name, role, password_hash) VALUES (?, ?, ?, ?, ?)');
        $ins->execute([$id, $email, $name, 'ADMIN', $hash]);
    }

    // Lock the installer so it can't run again.
    file_put_contents($lockPath, "Installed at " . date('c') . "\nDelete this file (and install.php) only if you really want to re-run the installer.\n");
    @chmod($lockPath, 0600);
}

