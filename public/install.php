function migrateSuccessHtml(array $applied): string
{
    if (empty($applied)) {
        $list = '<p class="muted">No new migrations to apply — schema was already up to date.</p>';
    } else {
        $items = implode('', array_map(fn ($f) => '<li><code>' . e($f) . '</code></li>', $applied));
        $list  = '<p>Applied:</p><ul>' . $items . '</ul>';
    }
    return "
        <div class=\"alert alert-ok\">Migrations finished.</div>
        <div class=\"card\">
            $list
            <a href=\"install.php?step=admin\" class=\"btn btn-primary\">Continue →</a>
        </div>";
}

