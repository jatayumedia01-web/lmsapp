function requirementsHtml(): string
{
    $checks = [
        ['PHP 8.2 or newer',          version_compare(PHP_VERSION, '8.2.0', '>='), PHP_VERSION],
        ['PDO MySQL extension',       extension_loaded('pdo_mysql'),               extension_loaded('pdo_mysql') ? 'loaded' : 'missing'],
        ['JSON extension',            extension_loaded('json'),                    extension_loaded('json') ? 'loaded' : 'missing'],
        ['mbstring extension',        extension_loaded('mbstring'),                extension_loaded('mbstring') ? 'loaded' : 'missing'],
        ['random_bytes available',    function_exists('random_bytes'),             function_exists('random_bytes') ? 'yes' : 'no'],
        ['Project root writable',     is_writable(dirname(__DIR__)),               is_writable(dirname(__DIR__)) ? 'yes' : 'no — chmod 755 on the project folder'],
    ];

    $rows = '';
    $allOk = true;
    foreach ($checks as [$label, $ok, $detail]) {
        if (!$ok) $allOk = false;
        $mark = $ok ? '<span class="check">✓</span>' : '<span class="cross">✗</span>';
        $rows .= "<li>$mark " . e($label) . " <span class=\"muted\">— " . e((string) $detail) . "</span></li>";
    }

    $cta = $allOk
        ? '<a href="install.php?step=db" class="btn btn-primary">Continue →</a>'
        : '<div class="alert alert-err">Fix the failed checks above before continuing. PHP version is the most common one — set it in hPanel → Websites → PHP Configuration.</div>';

    return "
        <div class=\"card\">
            <h1>Requirements check</h1>
            <ul>$rows</ul>
            $cta
        </div>";
}

